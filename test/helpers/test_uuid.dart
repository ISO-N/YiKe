/// 生成一个用于测试的稳定 UUID（伪造但格式合法）。
///
/// 说明：
/// - 仅用于单测场景，避免引入随机性导致断言不稳定
/// - [seed] 用于保证同一测试中多个 UUID 互不冲突
String testUuid([int seed = 1]) {
  final tail = seed.toString().padLeft(12, '0');
  return '00000000-0000-4000-8000-$tail';
}

